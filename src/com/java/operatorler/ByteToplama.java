package com.java.operatorler;

public class ByteToplama {
	
	public static void main(String[] args) {
		byte b1 = 3;
		byte b2 = 7;
		
		byte b3 = (byte) (b1 + b2);
		
		System.err.println(b3);
		
		System.err.println("------------");
		
		double d1 = 3;
		int i1 = 7;
		
		double i2 = d1 + i1;
		
		System.err.println(i2);
	}
}
